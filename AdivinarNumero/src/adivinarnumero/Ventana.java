    private void btnIntentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntentarActionPerformed
        // TODO add your handling code here:
      
       String ingreso = txtIntento.getText();
       if(!ingreso.equals("")){
      
       
       int intento= Integer.parseInt(ingreso);
       if(intento==randomico){
       
       lblMensaje.setText("Correcto !");
        txtIntento.setEditable(false); 
           
       
       }else if(intento<randomico){
       
       lblMensaje.setText("Muy bajo !");
       
       }else if(intento>randomico){
       
       lblMensaje.setText("Muy alto !"); 
       
       }
       
       if( intento>randomico-11 && intento<randomico+11 || intento==randomico){
       
       this.getContentPane().setBackground(Color.red); 
       
       }else{
       
       this.getContentPane().setBackground(Color.blue); 
       
       }
       }else{
       JOptionPane.showMessageDialog(rootPane, "Ingrese un numero !");
       
       }
       
    }//GEN-LAST:event_btnIntentarActionPerformed

